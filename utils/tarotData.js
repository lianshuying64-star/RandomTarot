// 获取所有牌
export function getAllCards() {
  return [
    ...tarotDeck.major,
    ...tarotDeck.wands,
    ...tarotDeck.cups, 
    ...tarotDeck.swords,
    ...tarotDeck.pentacles
  ].map(card => ({
    ...card,
    enName: card.enName || card.name // 🔥 确保有 enName
  }));
}

// 根据ID获取牌
export function getCardById(id) {
  const allCards = getAllCards();
  return allCards.find(card => card.id === id);
}

